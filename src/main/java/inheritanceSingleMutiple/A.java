package inheritanceSingleMutiple;

public class A {
	
	void method()
	{
		System.out.println("This is method 1 from classA");
	}
	void method(String name)
	{
		System.out.println("This is parameterized method form classA");
	}
	
	A()
	{
		System.out.println("This is constructor from classA");
	}
	
	A(String name)
	{
		System.out.println("This is parametrized constructor from classA");
	}

}
